def addLightGreenStrips(app,trackList):
    startX,startY = 2500, -1300
    width, height = 100000,100
    numDivs = 100
    for i in range(numDivs//2):
        polygon = [startX,startY,startX + width,startY,startX + width,startY + height,startX,startY+height]
        newPolygon = []
        for i in range(0,len(polygon),2):
            x, y = polygon[i],polygon[i + 1]
            radians = math.radians(25)
            cosTheta = math.cos(radians)
            sinTheta = math.sin(radians)
            pivotX = app.width / 2
            pivotY = app.height / 2
            tempX = x - pivotX
            tempY = y - pivotY
            rotatedX = tempX * cosTheta - tempY * sinTheta
            rotatedY = tempX * sinTheta + tempY * cosTheta
            finalX = rotatedX + pivotX
            finalY = rotatedY + pivotY
            newPolygon.append(finalX)
            newPolygon.append(finalY)
        newTuple = (newPolygon,'lightGreen')
        trackList.insert(1,newTuple)
        startY += height * 2
    return trackList
    
