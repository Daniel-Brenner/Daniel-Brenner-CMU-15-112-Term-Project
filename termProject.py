def adjustList(app,coordsList):
    res = []
    for i in range(0,len(coordsList),2):
        x, y = coordsList[i], coordsList[i + 1]
        x = x - app.carX + app.width/2
        y = y - app.carY + app.height/2
        x,y = rotateViaPolar(app,x,y,app.angle-90)
        res.append(x)
        res.append(y)
    return res

#this function was written with help from AI gemini
def rotateViaPolar(app,x,y,angle):
    radians = math.radians(angle)
    cosTheta = math.cos(radians)
    sinTheta = math.sin(radians)
    pivotX = app.width / 2
    pivotY = app.height / 2
    tempX = x - pivotX
    tempY = y - pivotY
    rotatedX = tempX * cosTheta - tempY * sinTheta
    rotatedY = tempX * sinTheta + tempY * cosTheta
    finalX = rotatedX + pivotX
    finalY = rotatedY + pivotY
    return finalX, finalY

