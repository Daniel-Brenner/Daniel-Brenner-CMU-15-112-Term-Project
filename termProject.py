def timeTrial_onStep(app):
    if app.timeTrialTimer < 0 and app.timeTrialTimer % 30 == 0 and app.timeTrialTimer > -160:
        app.lightsSound.play()
    if app.timeTrialTimer == 0:
        app.lightsOutSound.play()
    app.homeScreenSound.pause()
    if not app.paused and not app.crashed:
        controlCarStateOnStep(app)
        app.timeTrialTimer += 1
        curTrackCheckpoints = app.selectedTrack.checkPoints
        for i in range(len(curTrackCheckpoints)):
            x1,x2,y1,y2 = curTrackCheckpoints[i]
            if i == 0 and x1 <= app.carX <= x2 and y1 <= app.carY <= y2 and app.checkPointsCleared == set(curTrackCheckpoints):
                changeLaptimes(app)
                app.timeTrialTimer = 1
                app.checkPointsCleared = set([curTrackCheckpoints[i]])
                app.laps += 1
            elif i == 0 and x1 <= app.carX <= x2 and y1 <= app.carY <= y2 and app.checkPointsCleared != set(curTrackCheckpoints) and (len(app.checkPointsCleared) > 1 or app.timeTrialTimer > 600):
                app.timeTrialTimer = 1
                app.invalidLap = True
                app.checkPointsCleared = set([curTrackCheckpoints[0]])
                app.laps += 1
            else:
                if x1 <= app.carX <= x2 and y1 <= app.carY <= y2:
                    app.checkPointsCleared.add(curTrackCheckpoints[i])
            if app.laps >= 11:
                app.gameOver = True
        if app.timeTrialTimer > 100:
            app.invalidLap = False
        if app.gameOver:
            app.gameOverCounter += 1
    selectionCounterLogic(app)
    crashLogic(app)

def controlCarStateOnStep(app):
    if app.gameOver:
        if app.carSpeed > 0:
            app.carSpeed -= 0.25
        elif app.carSpeed < 0:
            app.carSpeed += 0.25
    else:
        if app.carSpeed > 0:
            app.carSpeed -= 0.125
        elif app.carSpeed < 0:
            app.carSpeed += 0.125
    app.carX += math.cos(math.radians(app.angle)) * app.carSpeed
    app.carY -= math.sin(math.radians(app.angle)) * app.carSpeed

def changeLaptimes(app):
    curLap = app.timeTrialTimer
    for i in range(len(app.lapTimes)):
        if app.lapTimes[i] == None:
            app.lapTimes[i] = curLap
            return
        if curLap < app.lapTimes[i]:
            app.lapTimes.insert(i,curLap)
            app.lapTimes.pop()
            return

def crashLogic(app):
    if app.crashed:
        app.gameOverCounter += 1
    if app.gameOverCounter > 160:
        app.crahsed = False
        setActiveScreen('gameOver')
    if not withinTrack(app):
        app.crashed = True
        app.gameOver = True
        app.carSpeed = 0

def withinTrack(app):
    curTrackZones = app.selectedTrack.zones
    for x1,x2,y1,y2 in curTrackZones:
        if x1 <= app.carX <= x2 and y1 <= app.carY <= y2:
            return True
    return False

